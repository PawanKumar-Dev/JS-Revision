// Iterations also called loops. The most basic loop is "for loop".
// First our index is assigned value. Then condition is checked. Then we jump to block scope where our data is console logged. After that, our index value is incremented. Then we check the condition again.
// Loop keep running until condition is false. Then we jump out of for loop.
for(let index = 1; index <= 5; index++) {
    console.log(index)
}