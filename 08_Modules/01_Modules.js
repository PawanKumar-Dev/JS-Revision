// Module is a file containing JS code that is executed in its own scope.
// Variables, functions, classes, etc., defined in a module aren't accessible outside unless explicitly exported.
// This encapsulation prevents pollution of the global namespace.


// Two module systems:

/*
  1. CommonJS Modules (used mainly in Node.js)
    Uses "require" for importing modules.
    Uses "module.exports" or "exports" for exporting modules.
    Loads modules synchronously.
*/

/*
  2. ES6 Modules (also known as ECMAScript Modules or ESM)
    Introduced as a standardized module system for JS.
    Uses "import" and "export" statements.
    Supports both synchronous and asynchronous loading (in browsers).
    Can be used in both client-side and server-side applications.
*/
